using System;

class Student
{
    private int _age;
    public int Age
    {
        get { return _age; }
        set { _age = value; }
    }

    private string _name;
    public string Name
    {
        get { return _name; }
        set { _name = value; }
    }

    private int _id;
    public int Id
    {
        get { return _id; }
        set { _id = value; }
    }
}
